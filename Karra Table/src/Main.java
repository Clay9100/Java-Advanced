public class Main {
    public static void main(String[] args) {
        // this is lesson Karra table

        // for

        for (int i = 2; i < 10 ; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.printf("%d x %d = %d\n", i, j, (i * j));
            }
            System.out.println();
        }


    }
}